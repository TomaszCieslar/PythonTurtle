import turtle
t = turtle.Turtle()
t.color('gold')
t.hideturtle()
t.pensize(10)
t.setheading(45)

t.left(90)
t.forward(200)
t.left(90)
t.forward(200)
t.left(90)
t.forward(200)
t.left(90)
t.forward(200)
t.forward(-200)


t.right(90)
t.forward(200)
t.right(90)
t.forward(200)
t.right(90)
t.forward(200)
t.right(90)
t.forward(200)
turtle.done()