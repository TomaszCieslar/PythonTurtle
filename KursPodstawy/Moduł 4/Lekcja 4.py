import turtle
t=turtle.Turtle()
t.hideturtle()
t.up()
t.goto(100,-200)
t.setheading(0)
t.down()
t.color('brown','brown')
t.begin_fill()
t.fd(50)
t.left(90)
t.fd(100)
t.left(90)
t.fd(50)
t.left(90)
t.fd(100)
t.left(90)
t.end_fill()


t.up()
t.goto(50,-100)
t.setheading(0)
t.down()
t.color('green','green')
t.begin_fill()
t.fd(150)
t.left(120)
t.fd(150)
t.left(120)
t.fd(150)

t.up()
t.goto(50,-50)
t.setheading(0)
t.down()
t.begin_fill()
t.fd(150)
t.left(120)
t.fd(150)
t.left(120)
t.fd(150)

t.up()
t.goto(50,0)
t.setheading(0)
t.down()
t.begin_fill()
t.fd(150)
t.left(120)
t.fd(150)
t.left(120)
t.fd(150)

t.end_fill()




turtle.done()