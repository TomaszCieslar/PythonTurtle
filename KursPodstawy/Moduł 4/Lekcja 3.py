import turtle

t=turtle.Turtle()
t.hideturtle()
t.up()
t.goto(0,-200)
t.setheading(0)
t.down()
t.color('blue','blue')
t.begin_fill()
t.fd(180)
t.left(90)
t.fd(200)
t.left(90)
t.fd(180)
t.left(90)
t.fd(200)
t.left(90)
t.end_fill()


t.up()
t.goto(50,-200)
t.setheading(0)
t.down()
t.color('green','green')
t.begin_fill()
t.fd(80)
t.left(90)
t.fd(130)
t.left(90)
t.fd(80)
t.left(90)
t.fd(130)
t.left(90)
t.end_fill()

t.up()
t.goto(0,0)
t.setheading(0)
t.down()
t.color('red','red')
t.begin_fill()
t.fd(180)
t.left(120)
t.fd(180)
t.left(120)
t.fd(180)
t.end_fill()


turtle.done()