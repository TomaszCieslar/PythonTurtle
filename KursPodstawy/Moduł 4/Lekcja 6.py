import turtle
t = turtle.Turtle()

t.hideturtle()
t.color('gray','gray')
t.up()
t.goto(-50,200)
t.down()
t.begin_fill()
t.fd(100)
t.right(90)
t.fd(300)
t.right(90)
t.fd(100)
t.right(90)
t.fd(300)
t.end_fill()

t.penup()
t.goto(0,100)
t.setheading(0)
t.color('red','red')
t.begin_fill()
t.circle(50)
t.end_fill()

t.penup()
t.goto(0,0)
t.setheading(0)
t.color('yellow','yellow')
t.begin_fill()
t.circle(50)
t.end_fill()

t.penup()
t.goto(0,-100)
t.setheading(0)
t.color('green','green')
t.begin_fill()
t.circle(50)
t.end_fill()

turtle.done()